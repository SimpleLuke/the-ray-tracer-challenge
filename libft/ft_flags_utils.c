#include "libft.h"

/* ************************************************************************** */
/*  ft_istype: Takes an integer character,                                    */
/*             Returns 1 if it is one of the flags.                           */
/*             Returns 0 if it is not one of the flags.                       */
/* ************************************************************************** */
int	ft_istype(int c)
{
	if (c == 'c' || c == 's' || c == 'd' || c == 'i'
		|| c == 'u' || c == 'x' || c == 'X' || c == 'p' || c == '%')
		return (1);
	return (0);
}
