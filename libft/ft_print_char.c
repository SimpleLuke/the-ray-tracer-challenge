#include "libft.h"

/* ************************************************************************** */
/*  ft_print_c: Takes a character and output on stdout.                       */
/*              Returns a count of 1.                                         */
/* ************************************************************************** */
int	ft_print_c(char c)
{
	write(1, &c, 1);
	return (1);
}
